def decode(code):
    string = ''
    characters = code.split()
    for x in characters:
        for y in MORSE:
            if x == MORSE.get(y):
                string += y
    return string

def encode(code):
    string = ''
    for char in code:
        string += MORSE.get(char) + " "
    return string;